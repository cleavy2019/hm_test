class Animal:

    def eat(self):
        print("吃")

    def drink(self):
        print("喝")

    def run(self):
        print("跑")

    def sleep(self):
        print("睡")

wangcai = Animal()
wangcai.eat()
wangcai.run()
wangcai.sleep()
wangcai.drink()

